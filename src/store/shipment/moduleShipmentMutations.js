export default {
  SET_PROVINCES(state, provinces) {
    state.provinces = provinces
  },

  SET_CITIES(state, cities) {
    state.cities = cities
  },

  SET_SUBDISTRICTS(state, subdistricts) {
    state.subdistricts = subdistricts
  },

  SET_META_PROVINCES(state, metaProvinces) {
    state.metaProvinces = metaProvinces
  },

  SET_META_CITIES(state, metaCities) {
    state.metaCities = metaCities
  },

  SET_META_SUBDISTRICTS(state, metaSubdistricts) {
    state.metaSubdistricts = metaSubdistricts
  },

}
